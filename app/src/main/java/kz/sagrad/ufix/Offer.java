public class Offer {
    public String phone =  "";
    public String name = "";
    public String email = "";
    public String price = "";
    public String comment = "";
    public Offer() {

    }

    public String getPhone() {
        return phone;
    }

    public String getEmail() {
        return email;
    }

    public String getPrice() {
        return price;
    }

    public String getComment() {
        return comment;
    }

    public String getName() {
        return name;
    }
}
